public class BasicMethod {
	
	public static int max(int a , int b) {
		return a>b?a:b;		
	}

	public static void sayMorining() {
		System.out.println("Morning!");
	}
	
	public class BasicMethodTest {
	
	public static void main(String[] args) {

		x = max(5,8);
		x = max(x,10);

		sayMorining();

	}
}

}